def get_all_bursa_tickers():
    """Return list of all Bursa Malaysia tickers in yfinance format."""
    return [f"{code}.KL" for code, name in BURSA_TICKERS]


def get_bursa_ticker_map():
    """Return dict mapping stock_code -> company_name."""
    return {code: name for code, name in BURSA_TICKERS}


def search_bursa(query):
    """Search for a company by name or code (case-insensitive)."""
    query = query.upper()
    return [(code, name) for code, name in BURSA_TICKERS
            if query in name.upper() or query in code]
